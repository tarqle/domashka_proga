# есть список животных в зоопарке

zoo = ['lion', 'kangaroo', 'elephant', 'monkey', ]

# посадите медведя (bear) между львом и кенгуру
# и выведите список на консоль
zoo.insert(1, 'bear')  # вставляем на позицию 1 (между lion (индекс 0) и kangaroo (индекс 1))
print(zoo)

# добавьте птиц из списка birds в последние клетки зоопарка
birds = ['rooster', 'ostrich', 'lark', ]
# и выведите список на консоль
zoo.extend(birds)  # добавляем всех птиц в конец списка
print(zoo)

# уберите слона
# и выведите список на консоль
zoo.remove('elephant')  # удаляем слона
print(zoo)

# выведите на консоль в какой клетке сидит лев (lion) и жаворонок (lark).
# Номера при выводе должны быть понятны простому человеку, не программисту.
# Нумерация клеток начинается с 1 (для людей)
lion_index = zoo.index('lion') + 1  # +1 для человеческой нумерации
lark_index = zoo.index('lark') + 1
print(f"Лев сидит в {lion_index}-й клетке")
print(f"Жаворонок сидит в {lark_index}-й клетке")
