# Есть строка с перечислением фильмов

my_favorite_movies = 'Терминатор, Пятый элемент, Аватар, Чужие, Назад в будущее'

# Выведите на консоль с помощью индексации строки, последовательно:
#   первый фильм
#   последний
#   второй
#   второй с конца

# Запятая не должна выводиться.  Переопределять my_favorite_movies нельзя
# Использовать .split() или .find()или другие методы строки нельзя - пользуйтесь только срезами,
# как указано в задании!

# Первый фильм (от начала до первой запятой)
print(my_favorite_movies[:10])

# Последний фильм (от последнего пробела после запятой до конца)
# Находим "Назад в будущее" - это последние 16 символов (включая пробел перед ним)
